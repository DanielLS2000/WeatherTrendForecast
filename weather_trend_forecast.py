def temp_and_precip_plot(df):
  plt.figure(figsize=(16, 6))

  # 1. Gráfico de Linha Temperatura
  plt.subplot(1, 2, 1)
  sns.lineplot(data=df, x='date_only', y='temperature_celsius', color='crimson', linewidth=2.5)
  plt.title('Global Temperature', fontsize=12, fontweight='bold')
  plt.xlabel('Date')
  plt.ylabel('Avg Temperature (°C)')
  plt.xticks(rotation=45)
  plt.grid(True, linestyle='--', alpha=0.6)

  # 2. Gráfico de Linha Precipitação
  plt.subplot(1, 2, 2)
  sns.lineplot(data=df_tendencia, x='date_only', y='precip_mm', color='royalblue', linewidth=2.5)
  plt.title('Global Precipitation', fontsize=12, fontweight='bold')
  plt.xlabel('Date')
  plt.ylabel('Avg Precipitation (mm)')
  plt.xticks(rotation=45)
  plt.grid(True, linestyle='--', alpha=0.6)

  plt.tight_layout()
  plt.show()
temp_and_precip_plot(df_tendencia)

def corr_matrix_plot(df, columns):
  matriz_correlacao = df[columns].corr()

  # Gerando o Heatmap
  plt.figure(figsize=(12, 10))
  sns.heatmap(
      matriz_correlacao,
      annot=True,
      cmap='coolwarm',
      fmt=".2f",
      linewidths=0.5,
      vmin=-1, vmax=1
  )

  plt.title('Correlation Matrix', fontsize=14, fontweight='bold', pad=20)
  plt.tight_layout()
  plt.show()

