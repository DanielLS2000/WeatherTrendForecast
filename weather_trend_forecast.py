print(df_clean[colunas_para_normalizar].head())

"""## Model Building"""

# Agrupando agora com as variáveis interessantes
colunas_interesse = ['temperature_celsius', 'humidity', 'uv_index', 'pressure_mb']
df_model = df_clean.groupby('date_only')[colunas_interesse].mean().reset_index()
df_model = df_model.sort_values('date_only')

# Features da variável principal
df_model['lag_1_temp'] = df_model['temperature_celsius'].shift(1)

# Features das variáveis extras
df_model['lag_1_humidity'] = df_model['humidity'].shift(1)
df_model['lag_1_uv'] = df_model['uv_index'].shift(1)
df_model['lag_1_pressure'] = df_model['pressure_mb'].shift(1)

# Limpando os valores nulos gerados pelos lags
df_model = df_model.dropna()

X = df_model[[
    'lag_1_temp', 'lag_1_humidity', 'lag_1_uv', 'lag_1_pressure'
]]
y = df_model['temperature_celsius']

# Train-test split
# Separando 80% dos dados mais antigos para treino, e os 20% mais recentes para teste
tamanho_treino = int(len(df_model) * 0.8)

X_train, X_test = X.iloc[:tamanho_treino], X.iloc[tamanho_treino:]
y_train, y_test = y.iloc[:tamanho_treino], y.iloc[tamanho_treino:]
datas_test = df_model['date_only'].iloc[tamanho_treino:]

print(f"Tamanho do Treino: {len(X_train)} dias")
print(f"Tamanho do Teste: {len(X_test)} dias")

# Training
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_absolute_error, mean_squared_error

modelo_lr = LinearRegression()
modelo_lr.fit(X_train, y_train)

# Predicting
previsoes = modelo_lr.predict(X_test)

# Metrics
mae = mean_absolute_error(y_test, previsoes)
mse = mean_squared_error(y_test, previsoes)
rmse = np.sqrt(mse)

print("--- Linear Regression Model ---")
print(f"MAE (Mean Absolute Error): {mae:.4f}")
print(f"MSE (Mean Squared Error): {mse:.4f}")
print(f"RMSE (Root Mean Squared Error): {rmse:.4f}")

# Plots and data viz
plt.figure(figsize=(14, 6))
plt.plot(datas_test, y_test.values, label='Real Values', color='royalblue', linewidth=2)

plt.plot(datas_test, previsoes, label='Model Prediction', color='darkorange', linestyle='--', linewidth=2)

plt.title('Linear Regression Model', fontsize=14, fontweight='bold')
plt.xlabel('Date')
plt.ylabel('Temperature (Normalized)')
plt.legend()
plt.grid(True, linestyle='--', alpha=0.6)
plt.xticks(rotation=45)

plt.tight_layout()
plt.show()

