# Redundant Stuff
to_remove = [
    'temperature_fahrenheit', 'wind_mph', 'pressure_in',
    'precip_in', 'feels_like_fahrenheit', 'visibility_miles', 'gust_mph'
]
df_clean = df.drop(columns=to_remove)

# Remove lines with too high values for some columns
df_clean = df_clean[
    (df_clean['temperature_celsius'] < 60) & (df_clean['temperature_celsius'] > -90) &
    (df_clean['wind_kph'] < 400) &
    (df_clean['pressure_mb'] < 1100) & (df_clean['pressure_mb'] > 850)
]

# Remove rows with impossible negative air quality values
df_clean = df_clean[
    (df_clean['air_quality_Carbon_Monoxide'] >= 0) &
    (df_clean['air_quality_Sulphur_dioxide'] >= 0) &
    (df_clean['air_quality_PM10'] >= 0)
]

